package demo1;

public class Printing_Numbers
{
	public static void main(String[] args) 
	{
	   int a=1;
	
	   while(a<=10)
	   {
		System.out.print(" "+a);
	   
		a++;
		
	   }
	   System.out.println("");
	   System.out.println("");
	   
	   int b=10;
	   
	   while(b>0)
	   {
		   System.out.print(b+" ");
		   
		   b--;
		   
	   }
	}
}
